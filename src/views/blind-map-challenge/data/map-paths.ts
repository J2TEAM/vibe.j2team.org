// ---------------------------------------------------------------------------
// Smoothing helpers
// ---------------------------------------------------------------------------

const SMOOTHNESS = 0.12

function r(n: number): number {
  return Math.round(n * 10) / 10
}

function catmullRomToBezier(points: number[][], smoothness = SMOOTHNESS): string {
  const n = points.length
  if (n < 3) return ''

  const p0 = points[0]!
  let d = `M${p0[0]},${p0[1]}`

  for (let i = 0; i < n; i++) {
    const pm1 = points[(i - 1 + n) % n]!
    const pi = points[i]!
    const pi1 = points[(i + 1) % n]!
    const pi2 = points[(i + 2) % n]!

    const cp1x = pi[0]! + (pi1[0]! - pm1[0]!) * smoothness
    const cp1y = pi[1]! + (pi1[1]! - pm1[1]!) * smoothness
    const cp2x = pi1[0]! - (pi2[0]! - pi[0]!) * smoothness
    const cp2y = pi1[1]! - (pi2[1]! - pi[1]!) * smoothness

    d += ` C${r(cp1x)},${r(cp1y)} ${r(cp2x)},${r(cp2y)} ${pi1[0]!},${pi1[1]!}`
  }

  return d + ' Z'
}

function centroid(pts: number[][]): [number, number] {
  let sx = 0
  let sy = 0
  for (const p of pts) {
    sx += p[0]!
    sy += p[1]!
  }
  return [Math.round(sx / pts.length), Math.round(sy / pts.length)]
}

